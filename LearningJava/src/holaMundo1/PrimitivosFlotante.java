package holaMundo1;

public class PrimitivosFlotante {

    public static void main(String[] args) {

        float miVariableFloat = 103.2F;

        System.out.println("--------------------------------------");
        System.out.println("La variable float es: " + miVariableFloat);
        System.out.println("El tamanio max es: " + Float.MAX_VALUE);
        System.out.println("El tamanio min es: " + Float.MIN_VALUE);

        double miVariableDouble = 100030.5;

        System.out.println("--------------------------------------");
        System.out.println("La variale double es: " + miVariableDouble);
        System.out.println("El tamanio max es: " + Double.MAX_VALUE);
        System.out.println("El tamanio min es: " + Double.MIN_VALUE);

    }
}
