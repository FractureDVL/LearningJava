package holaMundo1;

public class PrimitivosEnteros {

    public static void main(String[] args) {

        //tipos de datos primitivos en Java
        // byte, short, int, long.
        byte variableByte = 10;
        System.out.println("------------------------------");
        System.out.println("La variable byte es: " + variableByte);
        System.out.println("Tamanio max: " + Byte.MAX_VALUE);
        System.out.println("Tamanio min: " + Byte.MIN_VALUE);

        short variableShort = 300;
        System.out.println("------------------------------");
        System.out.println("La variable byte es: " + variableShort);
        System.out.println("Tamanio max: " + Short.MAX_VALUE);
        System.out.println("Tamanio min: " + Short.MIN_VALUE);

        short variableInteger = 4000;
        System.out.println("------------------------------");
        System.out.println("La variable int es: " + variableInteger);
        System.out.println("Tamanio max: " + Integer.MAX_VALUE);
        System.out.println("Tamanio min: " + Integer.MIN_VALUE);

        long variableLong = 400000000;
        System.out.println("------------------------------");
        System.out.println("La variable Long es: " + variableLong);
        System.out.println("Tamanio max: " + Integer.MAX_VALUE);
        System.out.println("Tamanio min: " + Integer.MIN_VALUE);

    }
}
